package BlockingQueue;

import java.util.Random;
import java.util.concurrent.BlockingQueue;
import java.util.concurrent.TimeUnit;

public class Consumer implements Runnable {
	private BlockingQueue<String> queue;
	private static final int DEFAULT_RANGE_FOR_SLEEP = 800;
	private String name;
	
	 public Consumer(BlockingQueue<String> queue) {
	        this.queue = queue;
	    }
	 public Consumer(String name,BlockingQueue<String> queue) {
	        this.queue = queue;
	        this.name=name;
	    }

	public void run() {
		System.out.println("启动<消费者线程>！  名称： "+name);
		Random r = new Random();
		boolean isRunning = true;
		try {
			while (isRunning) {
				System.out.println(name+" 正从队列获取数据...");
				String data = queue.poll(2, TimeUnit.SECONDS);
				if (null != data) {
					System.out.println(name+" 拿到数据：" + data);
					System.out.println(name+" 正在消费数据：" + data+ "      队列目前有" + queue.size() + "个数据");
					Thread.sleep(r.nextInt(DEFAULT_RANGE_FOR_SLEEP));
				} else {
					// 超过2s还没数据，认为所有生产线程都已经退出，自动退出消费线程。
					isRunning = false;
				}
			}
		} catch (InterruptedException e) {
			e.printStackTrace();
			Thread.currentThread().interrupt();
		} finally {
			System.out.println("退出消费者线程！ 名称："+name);
		}
	}
}
