package BlockingQueue;

import java.util.Random;
import java.util.concurrent.BlockingQueue;
import java.util.concurrent.TimeUnit;
import java.util.concurrent.atomic.AtomicInteger;

public class Producer implements Runnable {
	private volatile boolean isRunning = true;
	private BlockingQueue queue;
	private static AtomicInteger count = new AtomicInteger();
	private static final int DEFAULT_RANGE_FOR_SLEEP = 1000;
    private String name;
	
	
	public Producer(BlockingQueue queue) {
		this.queue = queue;
	}

	public Producer(String name,BlockingQueue queue) {
		this.queue = queue;
		this.name=name;
	}
	
	public void run() {
		String data = null;
		Random r = new Random();

		System.out.println("启动<生产者线程>！ 名称："+name);
		try {
			while (isRunning) {
				System.out.println(name+" 正在生产数据...");
				Thread.sleep(r.nextInt(DEFAULT_RANGE_FOR_SLEEP));

				data = "data:" + count.incrementAndGet();
				System.out.println(name+" 将数据：" + data + "放入队列..."+ "    队列目前有" + queue.size() + "个数据");
				
				if (!queue.offer(data, 2, TimeUnit.SECONDS)) {
					System.out.println(name+" 放入数据失败：" + data);
				}
			}
		} catch (InterruptedException e) {
			e.printStackTrace();
			Thread.currentThread().interrupt();
		} finally {
			System.out.println("退出生产者线程！  名称："+name);
		}
	}

	public void stop() {
		isRunning = false;
	}
}
